package com.gadwala.model;

public class Group {
	private int id;
	private String name;
	private int courseid;
	private int level;
	private Course course;
	public Group() {}
	
	public Group(String name, int courseid, int level) {
		super();
		this.name = name;
		this.courseid = courseid;
		this.level = level;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public int getCourseid() {
		return courseid;
	}

	public void setCourseid(int courseid) {
		this.courseid = courseid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public Course getCourse() {
		return course;
	}

	public void setCourse(Course course) {
		this.course = course;
	}
	
	
	
}
